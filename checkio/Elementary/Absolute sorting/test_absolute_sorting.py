    def test_Extra(self):
        assert checkio([3, -76]) == [3, -76]
        assert checkio([40, 11, 28, 99, 72, -23, 88, 15, 47, 68, 56, 93, 60,
                        -59, -18, -37, 27, -46, 53, 30]) == [11, 15, -18, -23, 27, 28, 30, -37, 40, -46, 47, 53, 56, -59, 60, 68, 72, 88, 93, 99]
        assert checkio([7]) == [7]
        assert checkio(
            [-68, 57, -58, 55, -99, 10, 14]) == [10, 14, 55, 57, -58, -68, -99]
        assert checkio([69, -40, 22, 79, 2, -26, -58, -96, 73, -46, -15, -59, 47]
                       ) == [2, -15, 22, -26, -40, -46, 47, -58, -59, 69, 73, 79, -96]
        assert checkio([35, -24, 83]) == [-24, 35, 83]
        assert checkio([32, -54, 50, -78, 9, -31, -11, -89, 16, -85, -79, -13, -68, -30, -53, 22, -25, 59, 24]
                       ) == [9, -11, -13, 16, 22, 24, -25, -30, -31, 32, 50, -53, -54, 59, -68, -78, -79, -85, -89]
        assert checkio([25, -69]) == [25, -69]
        assert checkio([66]) == [66]
        assert checkio([-97, 57, 52, 96]) == [52, 57, 96, -97]
        assert checkio([-41, 71, -99, 20, 2, 43, 64, 33, 23, 45, 16]
                       ) == [2, 16, 20, 23, 33, -41, 43, 45, 64, 71, -99]
        assert checkio([96, 7, -66, -25, 16, -8, -68, -41, 98, -99, 58, 88, 67, -45, 89, 31]
                       ) == [7, -8, 16, -25, 31, -41, -45, 58, -66, 67, -68, 88, 89, 96, 98, -99]
        assert checkio([62, 49, 13, 99, -75, 15, -74, -40, -72, -88, -66, 26, -71, 48]
                       ) == [13, 15, 26, -40, 48, 49, 62, -66, -71, -72, -74, -75, -88, 99]
        assert checkio(
            [2, -36, 42, -45, -67, 7, 86]) == [2, 7, -36, 42, -45, -67, 86]
        assert checkio([-70, -78, 42, -31, -28, 80, 5, 53,
                        90, -50, 86, 33, 60, 54]) == \
            [5, -28, -31, 33, 42, -50, 53, 54, 60, -70, -78, 80, 86, 90]
        assert checkio([32, 23, 45, 78]) == [23, 32, 45, 78]
        assert checkio([46, -83, 43, 28, -96, -84, -4, -21, 50, -65, -47]) == \
            [-4, -21, 28, 43, 46, -47, 50, -65, -83, -84, -96]
        assert checkio([56, -71, -46, 25, -45, 5, -72, 58, 48, 2]) == \
            [2, 5, 25, -45, -46, 48, 56, 58, -71, -72]

    def test_Basics(self):
        assert checkio([-20, -5, 10, 15]) == [-5, 10, 15, -20]
        assert checkio([1, 2, 3, 0]) == [0, 1, 2, 3]
        assert checkio([-1, -2, -3, 0]) == [0, -1, -2, -3]
